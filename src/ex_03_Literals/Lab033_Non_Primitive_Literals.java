package ex_03_Literals;

public class Lab033_Non_Primitive_Literals {
    public static void main(String[] args) {

        //Primitive Data type = defined by java people
        //Max, Min, Size is defined
        byte b = 10; // 1 byte -> 8 bits
        int age = 27; // 4 byte -> 32 bits

        //Non-Primitive (Defined by users), Reference data types
        //No max, min and size
        //8 Byte -> 64 bits

        String name = "Aditya"; // String is a bunch of chars
        int[] array_of_numbers = new int[10];

    }
}
