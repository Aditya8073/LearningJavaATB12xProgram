package ex_04_Operators;

public class Lab036_Unary_Operator {
    public static void main(String[] args) {
         //Unary Operator
        int a = 10;
        int b = -100;
        int result=a+b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(result);

        int c = -1;
        int c1=c+1;
        System.out.println(c1);
    }
}
