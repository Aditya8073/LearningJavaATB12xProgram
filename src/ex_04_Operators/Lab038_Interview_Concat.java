package ex_04_Operators;

public class Lab038_Interview_Concat {
    public static void main(String[] args) {
        String first_name = "Aditya";
        String last_name = "Verma";
        System.out.println(first_name+last_name); //Joins
        System.out.println("Full Name" + first_name+last_name);

        int a=10;
        int b=20;
        System.out.println(a+b); //Math

        // + -> behave differently with the data type.
        // + -> operator overloading
    }
}
