package ex_04_Operators;

public class Lab037_Relational_Operators {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        // All of them will return boolean output.

        int a = 25;
        int b = 90;
        boolean result = a>b;
        System.out.println(result);

        int myAge=27;
        int myBrotherAge=30;
        boolean result1 = myAge <= myBrotherAge;
        System.out.println(result1);

//        myAge < myBrotherAge or myAge = myBrotherAge


    }
}
